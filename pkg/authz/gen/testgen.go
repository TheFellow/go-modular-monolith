package main

import (
	"bytes"
	"fmt"
	"go/format"
	"strings"

	"github.com/cedar-policy/cedar-go/x/exp/schema/ast"
)

func renderModuleModelTests(tree *ast.Schema, moduleName, importPath string) ([]byte, error) {
	actionNS, actions, err := moduleActions(tree)
	if err != nil {
		return nil, fmt.Errorf("%s schema tests: %w", moduleName, err)
	}
	_, entityName, entityDef, err := moduleEntity(tree, actionNS, actions)
	if err != nil {
		return nil, fmt.Errorf("%s schema tests: %w", moduleName, err)
	}

	goEntity := exportedName(string(entityName))
	attrs := sortedFields(entityDef.Shape)
	var b bytes.Buffer
	fmt.Fprintf(&b, "// Code generated by authz/gen from %s. DO NOT EDIT.\n\n", schemaFile)
	b.WriteString("package authz_test\n\n")
	fmt.Fprintf(&b, "import (\n\t\"testing\"\n\n\tmoduleauthz %q\n", importPath)
	b.WriteString("\t\"github.com/TheFellow/go-modular-monolith/pkg/testutil\"\n\tcedar \"github.com/cedar-policy/cedar-go\"\n)\n\n")
	fmt.Fprintf(&b, "func Test%sCedarEntity(t *testing.T) {\n", goEntity)
	b.WriteString("\tt.Parallel()\n\n")
	fmt.Fprintf(&b, "\tmodel := moduleauthz.%s{\n", goEntity)
	b.WriteString("\t\tUID: cedar.NewEntityUID(\"Wrong::Type\", \"test-id\"),\n")
	for _, attr := range attrs {
		value, _, err := testValues(entityDef.Shape[attr].Type, string(attr))
		if err != nil {
			return nil, fmt.Errorf("%s schema tests: attribute %s: %w", moduleName, attr, err)
		}
		fmt.Fprintf(&b, "\t\t%s: %s,\n", exportedName(string(attr)), value)
	}
	b.WriteString("\t}\n\n")
	b.WriteString("\tgot := model.CedarEntity()\n")
	b.WriteString("\twant := cedar.Entity{\n")
	fmt.Fprintf(&b, "\t\tUID: cedar.NewEntityUID(moduleauthz.%sType, \"test-id\"),\n", goEntity)
	b.WriteString("\t\tParents: cedar.NewEntityUIDSet(),\n")
	b.WriteString("\t\tAttributes: cedar.NewRecord(cedar.RecordMap{\n")
	for _, attr := range attrs {
		_, value, err := testValues(entityDef.Shape[attr].Type, string(attr))
		if err != nil {
			return nil, err
		}
		fmt.Fprintf(&b, "\t\t\tmoduleauthz.%s%sAttr: %s,\n", goEntity, exportedName(string(attr)), value)
	}
	b.WriteString("\t\t}),\n\t\tTags: cedar.NewRecord(nil),\n\t}\n\n")
	b.WriteString("\ttestutil.Equals(t, got, want)\n")
	b.WriteString("}\n")

	return format.Source(b.Bytes())
}

func testValues(typ ast.IsType, attr string) (model, cedarValue string, err error) {
	goType, _, err := cedarType(typ)
	if err != nil {
		return "", "", err
	}
	id := strings.ToLower(attr)
	switch goType {
	case "string":
		return fmt.Sprintf("%q", "test-"+id), fmt.Sprintf("cedar.String(%q)", "test-"+id), nil
	case "int64":
		return "42", "cedar.Long(42)", nil
	case "bool":
		return "true", "cedar.Boolean(true)", nil
	case "cedar.EntityUID":
		value := fmt.Sprintf("cedar.NewEntityUID(\"Test::Reference\", %q)", "test-"+id)
		return value, value, nil
	default:
		return "", "", fmt.Errorf("unsupported Go type %s", goType)
	}
}
