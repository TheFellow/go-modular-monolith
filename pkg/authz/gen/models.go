package main

import (
	"bytes"
	"fmt"
	"go/format"
	"os"
	"path/filepath"
	"slices"
	"strings"
	"unicode"

	"github.com/cedar-policy/cedar-go/types"
	"github.com/cedar-policy/cedar-go/x/exp/schema"
	"github.com/cedar-policy/cedar-go/x/exp/schema/ast"
)

const schemaFile = "schema.cedarschema"

// generateModuleModels turns each module schema into its small Cedar boundary
// model. Domain models keep business behavior; generated models own only the
// authorization representation.
func generateModuleModels(modules []modulePolicy) {
	for _, module := range modules {
		path := filepath.Join(module.directory, schemaFile)
		src, err := os.ReadFile(path)
		must(err)

		var s schema.Schema
		s.SetFilename(path)
		must(s.UnmarshalCedar(src))
		resolved, err := s.Resolve()
		must(err)
		must(validatePolicies(resolved, filepath.Join(module.directory, "policies.cedar")))

		out, err := renderModuleModels(s.AST(), module.moduleName)
		must(err)
		must(os.WriteFile(filepath.Join(module.directory, "models_gen.go"), out, 0o644))
	}
}

func renderModuleModels(tree *ast.Schema, moduleName string) ([]byte, error) {
	actionNS, actions, err := moduleActions(tree)
	if err != nil {
		return nil, fmt.Errorf("%s schema: %w", moduleName, err)
	}
	entityNS, entityName, entityDef, err := moduleEntity(tree, actionNS, actions)
	if err != nil {
		return nil, fmt.Errorf("%s schema: %w", moduleName, err)
	}

	goEntity := exportedName(string(entityName))
	entityType := qualifyPath(entityNS, string(entityName))
	actionType := qualifyPath(actionNS, "Action")
	attrs := sortedFields(entityDef.Shape)

	var b bytes.Buffer
	fmt.Fprintf(&b, "// Code generated by authz/gen from %s. DO NOT EDIT.\n\n", schemaFile)
	b.WriteString("package authz\n\n")
	b.WriteString("import (\n\t_ \"embed\"\n\n\tcedar \"github.com/cedar-policy/cedar-go\"\n)\n\n")
	fmt.Fprintf(&b, "//go:embed %s\nvar Schema string\n\n", schemaFile)
	fmt.Fprintf(&b, "const (\n\t%sType cedar.EntityType = %q\n\tActionType cedar.EntityType = %q\n", goEntity, entityType, actionType)
	for _, attr := range attrs {
		fmt.Fprintf(&b, "\t%s%sAttr = %q\n", goEntity, exportedName(string(attr)), attr)
	}
	b.WriteString(")\n\n")

	b.WriteString("var (\n")
	actionNames := make([]string, 0, len(actions))
	for name := range actions {
		actionNames = append(actionNames, string(name))
	}
	slices.Sort(actionNames)
	for _, name := range actionNames {
		fmt.Fprintf(&b, "\tAction%s = cedar.NewEntityUID(ActionType, %q)\n", exportedName(name), name)
	}
	b.WriteString(")\n\n")

	fmt.Fprintf(&b, "// %s is the Cedar-facing authorization model for %s.\n", goEntity, entityType)
	fmt.Fprintf(&b, "type %s struct {\n\tUID cedar.EntityUID\n", goEntity)
	for _, attr := range attrs {
		field := entityDef.Shape[attr]
		if field.Optional {
			return nil, fmt.Errorf("entity %s attribute %s: optional attributes are not supported", entityType, attr)
		}
		goType, _, err := cedarType(field.Type)
		if err != nil {
			return nil, fmt.Errorf("entity %s attribute %s: %w", entityType, attr, err)
		}
		fmt.Fprintf(&b, "\t%s %s\n", exportedName(string(attr)), goType)
	}
	b.WriteString("}\n\n")

	fmt.Fprintf(&b, "// CedarEntity converts m to the entity shape declared in %s.\n", schemaFile)
	fmt.Fprintf(&b, "func (m %s) CedarEntity() cedar.Entity {\n", goEntity)
	fmt.Fprintf(&b, "\treturn cedar.Entity{\n\t\tUID: cedar.NewEntityUID(%sType, m.UID.ID),\n", goEntity)
	b.WriteString("\t\tParents: cedar.NewEntityUIDSet(),\n\t\tAttributes: cedar.NewRecord(cedar.RecordMap{\n")
	for _, attr := range attrs {
		_, converter, err := cedarType(entityDef.Shape[attr].Type)
		if err != nil {
			return nil, err
		}
		name := exportedName(string(attr))
		fmt.Fprintf(&b, "\t\t\t%s%sAttr: %s(m.%s),\n", goEntity, name, converter, name)
	}
	b.WriteString("\t\t}),\n\t\tTags: cedar.NewRecord(nil),\n\t}\n}\n")

	return format.Source(b.Bytes())
}

func moduleActions(tree *ast.Schema) (types.Path, ast.Actions, error) {
	var foundNS types.Path
	var found ast.Actions
	for ns, declarations := range tree.Namespaces {
		if len(declarations.Actions) == 0 {
			continue
		}
		if found != nil {
			return "", nil, fmt.Errorf("expected actions in one namespace")
		}
		foundNS, found = ns, declarations.Actions
	}
	if found == nil {
		return "", nil, fmt.Errorf("no actions declared")
	}
	return foundNS, found, nil
}

func moduleEntity(tree *ast.Schema, actionNS types.Path, actions ast.Actions) (types.Path, types.Ident, ast.Entity, error) {
	var resource ast.EntityTypeRef
	for _, action := range actions {
		if action.AppliesTo == nil || len(action.AppliesTo.Resources) != 1 {
			return "", "", ast.Entity{}, fmt.Errorf("each action must apply to exactly one resource type")
		}
		if resource == "" {
			resource = action.AppliesTo.Resources[0]
		} else if resource != action.AppliesTo.Resources[0] {
			return "", "", ast.Entity{}, fmt.Errorf("all actions must use the same resource type")
		}
	}

	path := string(resource)
	if !strings.Contains(path, "::") {
		path = string(actionNS) + "::" + path
	}
	idx := strings.LastIndex(path, "::")
	if idx < 0 {
		def, ok := tree.Entities[types.Ident(path)]
		if !ok {
			return "", "", ast.Entity{}, fmt.Errorf("resource entity %s is not declared", path)
		}
		return "", types.Ident(path), def, nil
	}
	ns, name := types.Path(path[:idx]), types.Ident(path[idx+2:])
	declarations, ok := tree.Namespaces[ns]
	if !ok {
		return "", "", ast.Entity{}, fmt.Errorf("resource namespace %s is not declared", ns)
	}
	def, ok := declarations.Entities[name]
	if !ok {
		return "", "", ast.Entity{}, fmt.Errorf("resource entity %s is not declared", path)
	}
	return ns, name, def, nil
}

func cedarType(typ ast.IsType) (goType, converter string, err error) {
	switch typ := typ.(type) {
	case ast.StringType:
		return "string", "cedar.String", nil
	case ast.LongType:
		return "int64", "cedar.Long", nil
	case ast.BoolType:
		return "bool", "cedar.Boolean", nil
	case ast.EntityTypeRef:
		return "cedar.EntityUID", "cedar.EntityUID", nil
	case ast.TypeRef:
		switch string(typ) {
		case "String", "__cedar::String":
			return "string", "cedar.String", nil
		case "Long", "__cedar::Long":
			return "int64", "cedar.Long", nil
		case "Bool", "__cedar::Bool":
			return "bool", "cedar.Boolean", nil
		default:
			return "cedar.EntityUID", "cedar.EntityUID", nil
		}
	default:
		return "", "", fmt.Errorf("unsupported Cedar type %T", typ)
	}
}

func sortedFields(record ast.RecordType) []types.String {
	fields := make([]types.String, 0, len(record))
	for name := range record {
		fields = append(fields, name)
	}
	slices.Sort(fields)
	return fields
}

func qualifyPath(ns types.Path, name string) string {
	if ns == "" {
		return name
	}
	return string(ns) + "::" + name
}

func exportedName(name string) string {
	var out strings.Builder
	upper := true
	for _, r := range name {
		if r == '_' || r == '-' || r == ' ' {
			upper = true
			continue
		}
		if upper {
			r = unicode.ToUpper(r)
			upper = false
		}
		out.WriteRune(r)
	}
	return out.String()
}
